package hardcoders808.bata.bank.backend.enums;

/**
 * Represents the physical or logical type of a bank card.
 */
public enum CardType {

    /**
     * A physical plastic card.
     */
    PHYSICAL,

    /**
     * A virtual card for online transactions only.
     */
    VIRTUAL,

    /**
     * A prepaid card with a pre-loaded balance.
     */
    PREPAID
}
