package hardcoders808.bata.bank.backend.enums;

/**
 * Represents the different types of bank accounts.
 */
public enum AccountType {

    /**
     * A savings account for long-term storage of funds.
     */
    SAVINGS,

    /**
     * A current (checking) account for daily transactions.
     */
    CURRENT,

    /**
     * An account specifically for junior or minor users.
     */
    JUNIOR,

    /**
     * An account for business entities.
     */
    BUSINESS
}
